public class ArithmeticOperator {

    public static void main(String[] args) {

        // Arithmetic - Shorthand operators ( +=, -=, *=, /=)
        // +, -, *, /, %


    int n1 = 10;
    int n2 = 20;

    n1 += n2; //n1 = n1 + n2; 

        int num = 10;
        num += 10; //=> num = num + 10
        System.out.println(num);

        num -= 10; // => num = num - 10
        System.out.println(num);

        num = num * 10; // num *= 10
        System.out.println(num);

        num /=  10; // num = num / 10
        System.out.println(num);

        //num = num *= 10 /= 30;

    }
}